package JavaBasics;

public class StringConcatenation {

	public static void main(String[] args) {
	 int a=100;
	 int b=200;
	 String X = "Hello";
	 String Z = "Rider";
	 System.out.println(a+b);
	 System.out.println(X+Z);
	 System.out.println(X+a+Z+b);
	 System.out.println("The output of a is:" +(a+b+Z));
	}

}
